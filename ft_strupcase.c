char	*ft_strupcase(char *str)
{
	int		i;
	int		c;

	c = 'a' - 'A';
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] = str[i] - c;
		i++;
	}
	return (str);
}
